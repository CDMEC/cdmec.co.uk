        <p>The club's locomotives for this include 'Coal Fired Steam', 'Electric' and 'Petrol' powered engines.</p>
        <p>Due to the track layout with passing loops several trains can be in operation together and it is normal to have two or three trains running on public days. This track is currently undergoing an extension including doubling of track, second signal box and new carrage shed.</p>
        <p>Completed and opened to the public in 1995, the track is 2000 feet long with more planned, the route normally used on public running days gives a six minute ride.</p>
        <p>On normal Sunday <a href="<?=base_url('railway/public-running')?>">public running days</a>, either the <a href="<?=base_url('railway/raised-track')?>">Raised Track</a>, or Ground Level track, will run, but on a few special occasions both tracks can be in operation together.</p>

